     */
    public function index()
    {
        $products = Product::latest();

        if(request('search_product')){
            $products->where('product_name', 'like', '%' . request('search_product') . '%')
                    ->orWhere('product_desc', 'like', '%' . request('search_product') . '%');
        }

        return view('product', [
            "title" => "Product",
            "active" => "product",
            // "product" => Product::all()\
            "product" => $products->paginate(8)
        ]);
    }

